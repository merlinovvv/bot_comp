function sendComplimentAt(targetTime) {
    const now = new Date().toLocaleString('ru-RU', { timeZone: 'Europe/Kiev' });
    const [currentDate, currentTime] = now.split(', ');
    const [currentHours, currentMinutes] = currentTime.split(':');
    const [targetHours, targetMinutes] = [targetTime.hours, targetTime.minutes];

    if (currentHours == targetHours && currentMinutes == targetMinutes) {
        sendCompliment();
    }

    // Calculate the time until the next target time
    const nextTargetTime = new Date();
    nextTargetTime.setHours(targetHours, targetMinutes, 0, 0);
    if (nextTargetTime <= new Date()) {
        // If the next target time is before the current time, add 1 day
        nextTargetTime.setDate(nextTargetTime.getDate() + 1);
    }
    const delay = nextTargetTime - new Date();

    setTimeout(() => {
        sendComplimentAt(targetTime); // Schedule next compliment
    }, delay);
}

